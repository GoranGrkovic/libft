#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t		d;
	size_t		f;

	d = 0;
	f = 0;
	while (s1[f])
		f++;
	while (s2[d] && d < n)
	{
		s1[f] = s2[d];
		d++;
		f++;
	}
	s1[f++] = '\0';
	return (s1);
}
